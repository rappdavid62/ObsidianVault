#!/usr/bin/env python3
"""
vault-watcher.py

A dependency-free polling directory watcher that automatically executes `sync-all.py`
whenever a file inside the Obsidian Prompt Library (09-PROMPTS/Library) is modified,
created, or deleted.

Run in the background to keep external AI surfaces aligned with Obsidian.
Before syncing, it runs vault-health-check.py --fail-on-attention and skips sync
when private-boundary, cleanup, source-truth, or tracked-deletion risks are open.
"""

import os
import sys
import time
import subprocess
from pathlib import Path

# Ensure UTF-8 output on Windows
if sys.platform == "win32":
    import io
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding="utf-8", errors="replace")

# Locate directories relative to the script location
TOOLS_DIR = Path(__file__).resolve().parent
LIBRARY_ROOT = TOOLS_DIR.parent
SYNC_SCRIPT = TOOLS_DIR / "sync-all.py"
HEALTH_SCRIPT = TOOLS_DIR / "vault-health-check.py"

# Files/folders to ignore during scan
IGNORE_PATTERNS = [
    "__pycache__",
    ".git",
    "Mobile-Favorites.md", # Generated by sync-all
    "master_context_latest.txt", # Generated by sync-all
]

def scan_files(directory: Path) -> dict[str, float]:
    """Scan directory and return a dict mapping file path to modified time."""
    file_map = {}
    for root, dirs, files in os.walk(directory):
        # Skip ignored directories in place
        dirs[:] = [d for d in dirs if d not in IGNORE_PATTERNS]
        
        for file in files:
            if file in IGNORE_PATTERNS or file.startswith("~") or file.endswith(".tmp"):
                continue
            path = Path(root) / file
            try:
                file_map[str(path.resolve())] = path.stat().st_mtime
            except Exception:
                pass
    return file_map

def health_gate_passes() -> bool:
    """Return True when the vault is safe enough for outbound sync."""
    print(f"[{time.strftime('%H:%M:%S')}] Running vault-health-check.py --fail-on-attention...")
    try:
        proc = subprocess.run(
            [sys.executable, str(HEALTH_SCRIPT), "--fail-on-attention"],
            cwd=str(TOOLS_DIR),
            text=True,
            capture_output=True,
            check=False,
        )
    except OSError as err:
        print(f"[ERROR] health gate could not run: {err}", file=sys.stderr)
        return False

    if proc.returncode == 0:
        print("Health gate passed.")
        return True

    print(f"[WARN] Health gate blocked sync with exit code {proc.returncode}.", file=sys.stderr)
    if proc.stderr:
        print(proc.stderr.strip(), file=sys.stderr)
    print("Run vault-health-check.py --write and review _Meta/Second Brain Health Report.md.", file=sys.stderr)
    return False


def run_sync():
    """Execute sync-all.py only when the strict health gate passes."""
    print(f"[{time.strftime('%H:%M:%S')}] File change detected.")
    if not health_gate_passes():
        return

    print(f"[{time.strftime('%H:%M:%S')}] Triggering sync-all.py...")
    try:
        proc = subprocess.run(
            [sys.executable, str(SYNC_SCRIPT)],
            cwd=str(TOOLS_DIR),
            text=True,
            capture_output=True,
            check=True,
        )
        print(proc.stdout)
    except subprocess.CalledProcessError as err:
        print(f"[ERROR] sync-all.py failed:\n{err.output}\n{err.stderr}", file=sys.stderr)

def main():
    print("==================================================")
    print("  OBSIDIAN PROMPT LIBRARY WATCHER INITIALIZED     ")
    print(f"  Watching: {LIBRARY_ROOT.resolve()}")
    print("==================================================")

    # Initial scan
    print("Performing initial scan...")
    current_state = scan_files(LIBRARY_ROOT)
    print(f"Tracking {len(current_state)} files. Watcher is active. Press Ctrl+C to stop.")

    try:
        while True:
            time.sleep(2)
            new_state = scan_files(LIBRARY_ROOT)
            
            # Check for changes, additions, or deletions
            changes = False
            for path, mtime in new_state.items():
                if path not in current_state or mtime != current_state[path]:
                    changes = True
                    break
            
            if not changes:
                for path in current_state:
                    if path not in new_state:
                        changes = True
                        break
            
            if changes:
                run_sync()
                current_state = new_state
                
    except KeyboardInterrupt:
        print("\nWatcher stopped by user.")
        sys.exit(0)

if __name__ == "__main__":
    main()
